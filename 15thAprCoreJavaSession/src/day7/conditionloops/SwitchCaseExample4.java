package day7.conditionloops;

public class SwitchCaseExample4 {

	public static void main(String[] args) {
		System.out.println("Program start");
		char ch='z';
		
		switch(ch) {
		case 'a':
			System.out.println("Given char is vowel");
			break;
		case 'e':
			System.out.println("Given char is vowel");
			break;
		case 'i':
			System.out.println("Given char is vowel");
			break;
		case 'o':
			System.out.println("Given char is vowel");
			break;
		case 'u':
			System.out.println("Given char is vowel");
			break;
		default:
			System.out.println("Given char is not a vowel");
			break;
		}

	}

}
